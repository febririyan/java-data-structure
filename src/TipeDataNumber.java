/* 
    Tipe data number dalam pemrograman java itu ada dua :
    a. Integer Number
    b. Floating Point Number
 */


public class TipeDataNumber {

    public static void main (String[] args)
    {

        /* ============= Jenis Tipe Data Number Integer ============= */

        byte IniByte = 100;
        short iniShort = 1000;
        int iniInt = 10000000;
        long iniLong = 1000000000;
        long iniLong2 = 100000000L;

        /* ============= Jenis Tipe Data Number Floating Number Point ============= */

        float iniFloat = 56.90F;
        double iniDouble = 7.6;

        /* ======== Literals ======== */

        int decimalInt = 56;
        int hexaDecimal = 0xFFFFF;
        int binaryDecimal = 0b101010;

        // Kita juga di java bisa menambahkan underscore

        int amount = 2_000_000;

    }
}
